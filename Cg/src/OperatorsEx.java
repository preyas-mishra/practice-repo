
public class OperatorsEx {

	public static void main(String[] args) {
		int firstNumber = 20; 
		int	secondNumber = 10;
		int addResult = firstNumber + secondNumber;
		int subResult = firstNumber - secondNumber;
		int multiplyResult = firstNumber * secondNumber;
		int divisionResult = firstNumber / secondNumber;
		int moduloResult = firstNumber % secondNumber;
		System.out.println("Addition is "+addResult);
		System.out.println("Subtraction is "+subResult);
		System.out.println("Multiplication is "+multiplyResult);
		System.out.println("Division is "+divisionResult);
		System.out.println("Modulus is "+moduloResult);
	}

}
